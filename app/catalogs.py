def label_choices(choices, code):
    """
    Функция: label_choices
    Назначение: Получить подпись по коду из кортежей (code, label).
    """
    for k, v in choices:
        if k == code:
            return v
    return code or ""


def choices_dict(choices):
    """Словарь код → подпись."""
    return dict(choices)
